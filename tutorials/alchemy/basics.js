// declaring an immutable variable
const a = 3;
console.log(`${a} \n`);

// declaring a mutable variable
let street = 'first';
street = 'second';
console.log(`${street} \n`);

// Types in JS - String, Number, Boolean
const isConfirmed = true;
const age = 42;
const description = 'very good';

// String interpolation - you use a backtick instead of quotes
const firstName = "Dan";
const secondName = 'Josh';
const message = `hello ${firstName}, this is ${secondName}`;
console.log(message);
